package com.example.demo.bean;

public class PageInfo {
	// 每页数量
	private int pageSize;
	// 当前页数下标
	private int index;
	
	
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	
	
	
}
